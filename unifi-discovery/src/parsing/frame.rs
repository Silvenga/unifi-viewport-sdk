use crate::parsing::error::ParsingError;
use crate::parsing::tlv_values::TlvValues;
use crate::parsing::Command;
use crate::Version;
use tracing::warn;

pub const PROTOCOL_VERSION: Version = 0x01;
pub const HEADER_LEN: usize = 4;

/// A raw discovery frame: command byte + TLV values.
///
/// Owns its data. Round-trippable — parsing bytes into a `Frame` and encoding
/// it back produces the same byte sequence (assuming canonical TLV ordering).
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Frame {
    /// The command byte.
    pub command: Command,
    /// The TLV values
    pub values: TlvValues,
}

impl Frame {
    /// Creates a frame with the given command and TLV values.
    pub fn new(command: Command, values: TlvValues) -> Self {
        Self { command, values }
    }

    /// Parses a complete discovery frame from `buf`.
    pub fn parse(buf: &[u8]) -> Result<Self, ParsingError> {
        if buf.len() < HEADER_LEN {
            return Err(ParsingError::BufferTooShort {
                needed: HEADER_LEN,
                available: buf.len(),
            });
        }

        let version = buf[0];
        let command = buf[1];
        let tlv_length = u16::from_be_bytes([buf[2], buf[3]]) as usize;

        if version != PROTOCOL_VERSION {
            return Err(ParsingError::UnsupportedVersion(version));
        }

        if buf.len() < HEADER_LEN + tlv_length {
            return Err(ParsingError::TlvLengthMismatch {
                declared: tlv_length,
                actual: buf.len().saturating_sub(HEADER_LEN),
            });
        }

        let mut tlv_section = &buf[HEADER_LEN..HEADER_LEN + tlv_length];
        let values = TlvValues::parse(&mut tlv_section)?;

        let trailing = buf.len() - (HEADER_LEN + tlv_length);
        if trailing > 0 {
            warn!(
                trailing_bytes = trailing,
                "Trailing bytes after TLV section, ignoring"
            );
        }

        Ok(Self { command, values })
    }

    /// Encodes the frame into a byte vector.
    ///
    /// If the total TLV section exceeds 65535 bytes or any individual TLV value
    /// exceeds 255 bytes, a warning is emitted and the oversized data is truncated
    /// to fit the protocol's length field constraints.
    pub fn encode(&self) -> Vec<u8> {
        let tlv_len = self.values.encoded_size();
        if tlv_len > u16::MAX as usize {
            warn!(
                tlv_len,
                "TLV section exceeds 65535 bytes, truncating to fit uint16 header"
            );
        }
        let mut buf = Vec::with_capacity(HEADER_LEN + tlv_len.min(u16::MAX as usize));

        buf.push(PROTOCOL_VERSION);
        buf.push(self.command);
        buf.extend_from_slice(&(tlv_len.min(u16::MAX as usize) as u16).to_be_bytes());

        self.values.write_to(&mut buf);

        buf
    }
}

